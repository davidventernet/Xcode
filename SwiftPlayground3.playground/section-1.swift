// Bool (Bolean: True/False, 0/1)

// True or False?

var truth : Bool
truth = true

// Math: Is it Greater or Less Than?

let myMath = 6 > 3
print(myMath)

// Or is it equal?

let one = 1
let myComparison = 1 > 2
print(myComparison)

// It is equal...

let five = 5
let myEqual = five == 5
print(myEqual)

// ! = The opposite of the Truth

let hisTruth = true
let myTruth = !hisTruth
print(myTruth)

// And: && = true if all are true

let bool1 = true
let bool2 = false
let result = bool1 && bool2
print(result)

// Or: || = true if one or more is true

let bool3 = true
let bool4 = false
let result2 = bool3 || bool4
print(result2)

